var a = 10

document.write(`<b>Result:</b>  <br> The value of a is : ${a} <br> ........................................... <br><br>` )

a = ++a

document.write(`<br> The value of ++a is : ${a} <br> Now the value of a is ${a} <br><br>` )

a = a++

document.write(`<br> The value of a++ is : ${a++} <br> Now the value of a is ${a} <br><br>` )

a = --a

document.write(`<br> The value of --a is : ${a} <br> Now the value of a is ${a} <br><br>` )


a = a--

document.write(`<br> The value of a-- is : ${a--} <br> Now the value of a is ${a} <br><br>` )